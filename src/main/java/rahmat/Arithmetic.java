public class Arithmetic {
    public static void main(String[] args) {
        int a = 5576, b = 667678;
        int s, diff, p, d, q, r;
        s = a + b;
        diff = a - b;
        p = a * b;
        q = a / b;
        r = a % b;
        System.out.println("Sum " + s);
        System.out.println("Difference " + diff);
        System.out.println(p);

        System.out.println(q);
        System.out.println(r);

    }
}

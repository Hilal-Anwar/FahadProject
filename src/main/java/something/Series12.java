package something;

public class Series12 {
    public static void main(String[] args) {
        for (int i = 1; i <= 1000; i++)
        {
            for (int j = 1; j <= 10; j++)
            {
                System.out.println(i + " X " + j + " = " + (i * j));
            }
            System.out.println();
        }
    }
}
